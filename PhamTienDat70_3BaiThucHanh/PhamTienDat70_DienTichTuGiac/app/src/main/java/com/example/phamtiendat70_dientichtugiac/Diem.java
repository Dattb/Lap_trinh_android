package com.example.phamtiendat70_dientichtugiac;

public class Diem {
    // bien
    private double X;
    private double Y;

    public Diem(double x, double y) {
        X = x;
        Y = y;
    }

    public Diem() {
    }

    public double getX() {
        return X;
    }

    public void setX(double x) {
        X = x;
    }

    public double getY() {
        return Y;
    }

    public void setY(double y) {
        Y = y;
    }
}
